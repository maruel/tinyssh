unsigned char key[crypto_core_aes128encrypt_KEYBYTES + 16];
unsigned char out[crypto_core_aes128encrypt_OUTPUTBYTES + 16];
unsigned char in[crypto_core_aes128encrypt_INPUTBYTES + 16];

