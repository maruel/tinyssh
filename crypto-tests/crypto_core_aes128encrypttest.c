void test_alignment(void)
{

    long long i;

    for (i = 0; i < 16; ++i) {
        crypto_core_aes128encrypt(out + i, in + i, key + i, 0);
    }
}

static void test_rand(void)
{

    long long i;

    for (i = 0; i < 256; ++i) {
        pseudorandombytes(key, sizeof key);
        pseudorandombytes(in, sizeof in);
        crypto_core_aes128encrypt(out, in, key, 0);
        checksum(out, crypto_core_aes128encrypt_OUTPUTBYTES);
    }
    fail_whenbadchecksum(o);
}

