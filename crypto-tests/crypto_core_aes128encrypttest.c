static void test_vector(void)
{

    long long i, j;
    unsigned char out[crypto_core_aes128encrypt_OUTPUTBYTES];

    for (i = 0; i < 4; ++i) {
        crypto_core_aes128encrypt(out, vectors[i].in, vectors[i].key, 0);
        for (j = 0; j < crypto_core_aes128encrypt_OUTPUTBYTES; ++j)
            if (out[j] != vectors[i].out[j])
                fail("crypto_core_aes128encrypt() failure");
    }
}

